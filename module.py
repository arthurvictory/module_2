def house():
    l = int(input("Enter your length: "))
    w = int(input("Enter your width: "))
    squared = l * w
    print(f"The squared footage of the house is {squared}")

def circle():
    PI = 3.14
    r = float(input("Enter the radius of a circle:"))
    area = PI * r * r
    print(f"The area of the circle is {area}")
    